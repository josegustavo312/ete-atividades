package Aula;

public class Concatenação {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		int numero = 2;
		float numero2 = 5;
		String nome = "OI";
		
		System.out.printf ("%d\n", numero); //para concatenar o int
		
		System.out.printf("%f\n", numero2); //para concatenar o float e double
		
		System.out.printf("\t%s", nome);
		/*usa-se a porcetagem para concatenar a variável string
		 * e o comando /t para criar uma tabulação */

	}

}
